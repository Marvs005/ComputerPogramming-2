
public class Attendance {
	
	int daysPresent;
	int daysAbsent;
	
	public Attendance (int daysAbsent, int daysPresent) {
		this.daysAbsent = daysPresent;
		this.daysAbsent = daysAbsent;
		
		
	}
	public int setdaysPresent() {
		return daysPresent;
	}
	
	public int setdaysAbsent() {
		return daysAbsent;
	}

}
